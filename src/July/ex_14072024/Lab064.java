package July.ex_14072024;

public class Lab064 {
    public static void main(String[] args) {
        //  Ternary operator.
        //  ? if true do this : ele do that
        int result = 10>40 ? 89 : 91;
        System.out.println(result); // O/P: 91
        int a1 = (30 > 40) ? 10 : 20;
        System.out.println(a1); // O/P: 20

        String str = 10 > 20 ? "10" : "TWENTY";
        System.out.println(str); // O/P: TWENTY

    }
}
