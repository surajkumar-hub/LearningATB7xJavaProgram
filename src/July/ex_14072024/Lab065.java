package July.ex_14072024;

public class Lab065 {
    public static void main(String[] args) {
        // Find the max between two numbers by using Ternary operator
        int a= 10;
        int b= 20;
        int max = a > b ? a : b;
        int min = a < b ? a : b;
        System.out.println(max);
        System.out.println(min);
    }
}
